// 지역 목록 생성
export const getRegions = (): { doNm: string; count: number }[] => {
    const regionMap = new Map<string, number>();
    mockCampsites.forEach(camp => {
        const count = regionMap.get(camp.doNm) || 0;
        regionMap.set(camp.doNm, count + 1);
    });
    return Array.from(regionMap.entries()).map(([doNm, count]) => ({ doNm, count }));
};

